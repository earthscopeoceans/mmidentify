subroutine getfe(fein,feout,nrfe)

! Given a Flynn-Engdahl region fein (as printed by FetchEvent), find
! The Flynn-Enghdahl region number nrfe and return a shorthand version
! feout
! nrfe=0 if no match has been found


implicit none
character*32, intent(in) :: fein
character*16, intent(out) :: feout
integer, intent(out) :: nrfe
integer :: i

if(longfe(1)(1:7).ne.'CENTRAL') then
  call fillfe
endif  

feout=' '
nrfe=0
do i=1,757
  if(trim(longfe(i)).eq.trim(fein)) then
    nrfe=i
    exit
  endif
enddo
if(nrfe>0) feout=shortfe(nrfe)

return
end subroutine getfe

